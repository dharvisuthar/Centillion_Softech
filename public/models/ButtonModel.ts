/**
 * export class for button model
 */
export class ButtonModel {

    /**
     * stores text of button
     */
    Text: string;

    /**
     * stores component of button
     */
    Component: string;

    /**
     * stores name of button
     */
    Name: string;
    
    /**
     * check either button should active or not
     */
    Active: boolean;
}